package cmderror

import "fmt"

type CmdError struct {
	Code    int    `json:"code"`    // exit code
	Message string `json:"message"` // exit message
}

func (e CmdError) Format(args ...interface{}) CmdError {
	e.Message = fmt.Sprintf(e.Message, args...)
	return e
}
